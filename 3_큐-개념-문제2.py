# 문제2
# N명의 사람들이 원을 이루어 앉아 있습니다. 
# 특정 숫자 K가 주어지면, 첫 번째 사람부터 시작하여 K번째 사람을 원에서 제거합니다. 
# 그리고 제거된 사람 다음 사람부터 다시 K번째 사람을 제거하는 과정을 원에 한 명이 남을 때까지 반복합니다. 
# 이 문제의 핵심은 '정해진 순서대로 처리'하고, '맨 뒤의 요소가 다시 맨 앞으로 오는' 순환 구조를 효율적으로 다루는 것입니다. 
# 이 문제 해결에 가장 적합한 자료구조는 무엇이라고 생각하며, 그 이유는 무엇인가요? 
# 선택한 자료구조가 이 문제의 순차적, 순환적 특징을 어떻게 처리하는지 동작 과정을 단계별로 설명하세요.

# 답변:
# 이 문제를 해결하는 데 가장 적합한 자료구조는 '원형 큐(Circular Queue)'이다.
# 원형 큐는 배열의 끝이 다시 시작으로 연결되어 있는 구조로, 순환적인 특성을 효율적으로 처리할 수 있다.
# 원형 큐를 사용하면, K번째 사람을 제거한 후에도 큐의 앞부분에 비어 있는 공간이 생기더라도,
# rear 포인터가 그 공간을 다시 사용할 수 있게 된다.
# 이 문제의 동작 과정을 단계별로 설명하면 다음과 같다:
# 1. N명의 사람을 원형 큐에 순서대로 삽입한다.
# 2. 첫 번째 사람부터 시작하여 K번째 사람을 찾기 위해 front 포인터를 이동시킨다.
# 3. K번째 사람을 제거(dequeue)하고, 그 사람의 다음 사람부터 다시 K번째 사람을 찾기 위해 front 포인터를 이동시킨다.
# 4. 이 과정을 원에 한 명이 남을 때까지 반복한다.
# 5. 마지막으로 남은 사람을 반환한다.

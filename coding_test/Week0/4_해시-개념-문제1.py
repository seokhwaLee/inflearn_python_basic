# 문제1
# 해시 함수를 만드는 가장 간단한 방법 중 하나는 나눗셈법 (h(k) = k mod m) 입니다. 
# 여기서 m은 해시 테이블의 크기를 의미합니다. 
# 이때, m의 값으로 2의 거듭제곱 (예: 2p)이나 10의 거듭제곱을 피하고 
# 소수(prime number)를 사용하는 것이 좋다고 알려져 있습니다. 그 이유는 무엇인가요? 
# m의 값에 따라 입력 키(key)들이 특정 패턴을 가질 경우, 해시 충돌이 어떻게 빈번하게 발생할 수 있는지 그 원리를 설명하세요.

# [힌트]
# 공약수: m과 키(key) 값들 사이의 관계
# 키의 특정 패턴: 입력 키가 모두 짝수이거나 특정 배수일 경우
# 하위 비트(Lower Bits): m이 2의 거듭제곱일 때 mod 연산의 결과
# 데이터 쏠림 현상(Clustering) 과 균등한 분포

# 답변:
# m의 값으로 2의 거듭제곱이나 10의 거듭제곱을 피하고 소수를 사용하는 것이 좋은 이유는 해시 충돌을 줄이기 위해서이다.
# 2의 거듭제곱이나 10의 거듭제곱을 사용하면, 입력 키들이 특정 패턴을 가질 경우 해시 충돌이 빈번하게 발생할 수 있다.
# 예를 들어, m이 2의 거듭제곱일 때, 모든 짝수 키들은 동일한 해시 값을 가지게 되어 충돌이 발생할 수 있다.
# 이는 하위 비트만을 고려하기 때문에, 키 값들이 특정 패턴을 가질 경우 데이터 쏠림 현상이 발생할 수 있다.
# 반면에, 소수를 사용하면, 입력 키들이 다양한 패턴을 가질 때도 해시 값이 균등하게 분포될 가능성이 높아져 충돌이 줄어든다.
# 문제2
# 시간 복잡도를 빅오 표기법으로 나타낼 때, 왜 상수 계수와 영향력이 가장 작은 항(낮은 차수의 항)을 무시하나요? 
# 예를 들어, 알고리즘의 정확한 연산 횟수가 3n² + 10n + 5 라면, 
# 이를 O(n²)으로 단순화하는 이유를 '입력 크기 n이 무한히 커지는 극한'의 관점에서 설명하세요.

# 답변:
# 빅오 표기법은 알고리즘의 시간 복잡도를 표현하는 데 사용되는 수학적 표기법으로, 
# 주로 입력 크기 n이 무한히 커질 때 알고리즘의 실행 시간이 어떻게 증가하는지를 설명한다. 
# 상수 계수와 영향력이 가장 작은 항(낮은 차수의 항)을 무시하는 이유는 
# 이러한 요소들이 입력 크기 n이 매우 커질 때 알고리즘의 성장률에 미치는 영향이 상대적으로 작아지기 때문이다.
# 예를 들어, 알고리즘의 정확한 연산 횟수가 3n² + 10n + 5 라고 할 때, 
# n이 작을 때는 10n이나 5와 같은 항들이 전체 연산 횟수에 상당한 영향을 미칠 수 있다. 
# 그러나 n이 무한히 커질수록 n² 항이 전체 연산 횟수에서 지배적인 역할을 하게 된다. 
# 이는 n이 커질수록 3n²가 10n과 5에 비해 훨씬 더 크게 증가하기 때문이다.
# 따라서, 빅오 표기법에서는 이러한 상수 계수와 낮은 차수의 항을 무시하고, 
# 알고리즘의 성장률을 나타내는 가장 큰 차수의 항만을 고려한다. 

# 문제1
# 어떤 알고리즘의 시간 복잡도가 O(n²)이라고 할 때, 
# 이것이 "입력 크기 n이 작을 때 n²에 비례하는 연산을 하는 다른 알고리즘보다 항상 느리다"는 것을 의미할까요? 
# 그렇지 않다면, 빅오 표기법이 실제 실행 시간을 보장하는 것이 아님에도 불구하고 알고리즘의 효율성을 판단하는 핵심 척도로 사용되는 이유는 무엇인지, 
# '성장률(Growth Rate)'의 관점에서 설명하세요.

# 답변:
# 빅오 표기법은 알고리즘의 시간 복잡도를 나타내는 수학적 표기법으로, 입력 크기 n이 무한히 커질 때 알고리즘의 실행 시간이 어떻게 증가하는지를 설명한다. 
# O(n²)라는 표기법은 입력 크기 n이 충분히 클 때, 알고리즘의 실행 시간이 n²에 비례하여 증가한다는 것을 의미한다. 
# 그러나 이것이 항상 n²에 비례하는 다른 알고리즘보다 느리다는 것을 의미하지는 않는다.
# 실제 실행 시간은 상수 시간, 낮은 차수의 항, 하드웨어 성능, 구현 세부 사항 등 다양한 요인에 의해 영향을 받을 수 있다.
# 예를 들어, O(n²) 알고리즘이 상수 시간이 매우 작고 최적화가 잘 되어 있다면, 
# 작은 입력 크기에서는 O(n) 알고리즘보다 빠를 수 있다.
# 빅오 표기법이 알고리즘의 효율성을 판단하는 핵심 척도로 사용되는 이유는 '성장률(Growth Rate)'에 있다. 
# 성장률은 입력 크기가 커짐에 따라 알고리즘의 실행 시간이 어떻게 변화하는지를 나타낸다. 
# 이는 알고리즘의 성능을 비교할 때 중요한 기준이 된다. 
# 입력 크기가 작을 때는 상수 시간이나 낮은 차수의 항이 더 큰 영향을 미칠 수 있지만, 
# 입력 크기가 커질수록 성장률이 지배적인 역할을 하게 된다. 
# 따라서, 빅오 표기법은 알고리즘의 장기적인 성능을 예측하고 비교하는 데 유용한 도구로 사용된다. 
# 결국, 빅오 표기법은 알고리즘의 효율성을 평가하는 데 있어 중요한 역할을 하며, 

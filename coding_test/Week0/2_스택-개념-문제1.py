# 문제1
# 재귀가 없는 일반적인 상황을 가정해 봅시다. 
# 함수 A가 함수 B를 호출하고, 함수 B가 함수 C를 호출했을 때,
# 컴퓨터 내부의 호출 스택(Call Stack) 에는 어떤 정보들이 어떤 순서로 쌓이고(Push) 해제(Pop)되는지 그 과정을 설명하세요.
# 이를 통해 호출 스택이 LIFO(Last-In, First-Out) 원리로 동작하는 이유를 서술하세요.

# 답변:
# 호출 스택(Call Stack)은 함수 호출과 관련된 정보를 저장하는 메모리 구조이다.
# 함수 A가 호출되면, 호출 스택에 함수 A의 실행 컨텍스트(매개변수, 지역 변수, 반환 주소 등)가 쌓인다(Push).
# 그 후, 함수 A가 함수 B를 호출하면, 함수 B의 실행 컨텍스트가 호출 스택에 쌓인다.
# 마찬가지로, 함수 B가 함수 C를 호출하면, 함수 C의 실행 컨텍스트가 호출 스택에 쌓인다.
# 이제 함수 C의 실행이 완료되면, 호출 스택에서 함수 C의 실행 컨텍스트가 제거된다(Pop).
# 그 다음, 함수 B의 실행이 완료되면, 호출 스택에서 함수 B의 실행 컨텍스트가 제거된다.
# 마지막으로, 함수 A의 실행이 완료되면, 호출 스택에서 함수 A의 실행 컨텍스트가 제거된다.
# 이 과정에서 호출 스택은 LIFO(Last-In, First-Out) 원리로 동작한다.
# 즉, 가장 마지막에 호출된 함수가 가장 먼저 실행을 완료하고 스택에서 제거되기 때문이다.
# 이러한 LIFO 구조는 함수 호출과 반환의 순서를 자연스럽게 관리할 수 있게 해준다.
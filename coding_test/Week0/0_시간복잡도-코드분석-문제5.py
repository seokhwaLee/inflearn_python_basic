# n개의 원소를 가진 데이터 컬렉션에서 맨 앞의 원소를 제거하는 연산을 수행려고 한다. 
# 파이썬의 list에서 pop(0)를 사용하는 것과 collections.deque에서 popleft()를 사용하는 것의 시간 복잡도를 빅오(Big-O) 표기법으로 각각 서술하고,
# 왜 성능 차이가 발생하는지 각 자료구조의 내부 동작 원리와 연관 지어 설명.

# 1. my_list.pop(0) (단, my_list는 n개의 원소를 가진 리스트)
# list 자료구조에서 pop(0) 연산은 첫 번째 원소를 제거한 후, 나머지 원소들을 한 칸씩 앞으로 이동시켜야 함.
# 예를 들어, my_list가 [a, b, c, d]일 때
# my_list.pop(0)을 수행하면 a가 제거되고, 리스트는 [b, c, d]가 되며,
# 나머지 원소들이 한 칸씩 앞으로 이동해야 하므로, 시간 복잡도는 O(n).

# 2. my_deque.popleft() (단, my_deque는 n개의 원소를 가진 deque)
# collections.deque 자료구조에서 popleft() 연산은 양쪽 끝에서 빠르게 원소를 추가하거나 제거할 수 있도록 설계됨.
# 내부적으로 이중 연결 리스트(doubly linked list)로 구현되어 있어,
# 첫 번째 원소를 제거할 때 나머지 원소들을 이동시킬 필요가 없음.
# 따라서 popleft() 연산의 시간 복잡도는 O(1).
# 문제3
# 다음 빅오 표기법들을 실행 시간이 가장 빠른 것부터 느린 순서대로 나열하고, 
# 각 표기법이 나타내는 대표적인 알고리즘이나 연산의 예를 하나씩 들어 설명하세요: 
# O(1), O(log n), O(n), O(n log n), O(n²), O(2ⁿ)

# 답변:
# 빅오 표기법을 실행 시간이 가장 빠른 것부터 느린 순서: O(1), O(log n), O(n), O(n log n), O(n²), O(2ⁿ)
# 1. O(1) - 상수 시간 복잡도
#  - 입력 크기 n에 관계없이 실행 시간이 일정한 알고리즘
#  - 대표적인 예: 배열에서 특정 인덱스의 요소에 접근하는 연산

# 2. O(log n) - 로그 시간 복잡도
#  - 입력 크기 n이 증가함에 따라 실행 시간이 로그 함수처럼 증가하는 알고리즘
#  - 대표적인 예: 이진 탐색 알고리즘 (정렬된 배열에서 특정 값을 찾을 때, 매 단계마다 탐색 범위를 절반으로 줄여나감)

# 3. O(n) - 선형 시간 복잡도
#  - 입력 크기 n에 비례하여 실행 시간이 증가하는 알고리즘
#  - 대표적인 예: 배열의 모든 요소를 한 번씩 순회하는 알고리즘 (예: 최대값 찾기)

# 4. O(n log n) - 선형 로그 시간 복잡도
#  - 입력 크기 n에 비례하여 로그 함수처럼 증가하는 알고리즘
#  - 대표적인 예: 병합 정렬(Merge Sort) 알고리즘 (분할 정복 기법을 사용하여 배열을 정렬)

# 5. O(n²) - 이차 시간 복잡도
#  - 입력 크기 n의 제곱에 비례하여 실행 시간이 증가하는 알고리즘
#  - 대표적인 예: 버블 정렬(Bubble Sort) 알고리즘 (두 개의 중첩된 반복문을 사용하여 배열을 정렬)

# 6. O(2ⁿ) - 지수 시간 복잡도
#  - 입력 크기 n이 증가함에 따라 실행 시간이 지수 함수처럼 증가하는 알고리즘
#  - 대표적인 예: 피보나치 수열을 재귀적으로 계산하는 알고리즘 (중복 계산이 많아 입력 크기가 조금만 커져도 실행 시간이 급격히 증가)